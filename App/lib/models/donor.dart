class Donor {
  final String id;
  final String name;
  final String email;
  final String bloodGroup;
  final String city;
  final int age;
  final bool availability;

  Donor({
    required this.id,
    required this.name,
    required this.email,
    required this.bloodGroup,
    required this.city,
    required this.age,
    required this.availability,
  });
}
